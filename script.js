const updateExchangerRate = () => {
    let amount = document.querySelector(".amount input");
    let amtVal = amount.value;
    if (amtVal === " " || amtVal < 1) {
        amtVal = 1;
        amount.value = "1";
    }

    //console.log(fromCurr.value, toCurr.value)
    const URL = `https://api.currencyapi.com/v3/latest?apikey=cur_live_y5gu5xbGKjVSmSKrh7BNWJJKHecyIzCtSw3PlwDZ&base_currency=${fromCurr.value}&currencies=${toCurr.value}`
    fetch(URL)
        .then(response => response.json()) // Parse the JSON response
        .then(data => {
            console.log(data);
            const countryName = toCurr.value;
            const rate = data.data[countryName].value; // Access the INR value
            console.log(rate); // Output only the INR value
            let finalAmount = amtVal * rate;
            console.log(finalAmount)
            msg.innerText = `${amtVal} ${fromCurr.value} = ${finalAmount} ${toCurr.value}`   // `1USD = 80INR`
        })
        .catch(error => console.error("Error fetching data:", error));
}





